const statusColorProcess = (state) => {
  let color
  switch (state) {
    case `tag`:
      color = `#HEXCOLORHERE`
      break;

    case `voucher`:
      color = `#HEXCOLORHERE`
      break;

    case `approve`:
      color = `#HEXCOLORHERE`
      break;

    case `transmit`:
      color = `#HEXCOLORHERE`
      break;

    case `cheque`:
      color = `#HEXCOLORHERE`
      break;

    case `release`:
      color = `#HEXCOLORHERE`
      break;

    case `file`:
      color = `#HEXCOLORHERE`
      break;

    case `reverse`:
      color = `#HEXCOLORHERE`
      break;

    case `clear`:
      color = `#HEXCOLORHERE`
      break;

    default:
      color = `#HEXCOLORHERE`
  }

  return color
}

export default statusColorProcess